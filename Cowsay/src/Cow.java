public class Cow {
    private String name;
    private String image;

    //Set name of cow
    public Cow (String name) {
        this.name = name;
    }

    //Get name of cow
    public String getName() {
        return this.name;
    }

    //Get cow image
    public String getImage() {
        return this.image;
    }

    //Set cow image
    public void setImage (String _image) {
        this.image = _image;
    }

}
